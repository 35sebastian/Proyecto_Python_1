#Initialize the String
String = ['P', 'r', 'e', 'p', 'I', 'n', 's', 't', 'a']

#Slicing using 1 negative index
arr = String[-2:]
print(arr)

#Slicing using 1 negative index
arr = String[:-3]
print(arr)

#Slicing using 3rd negative index
arr = String[::-1]
print(arr)

#Slicing using 2 negative indexes
arr = String[-9:-4]
print(arr)